class Comparission {
public static void main(String []args) {
int a = 10;
int b = 20;
int c = 5;
int d = 2;
System.out.println("Equal Result is: "+(a==b));
System.out.println("Not Equal Result is: "+(a!=b));
System.out.println("Greater Than Result is: "+(c>d));
System.out.println("Less Than Result is: "+(a<d));
System.out.println("Greater Than or Equal Result is: "+(a>=d));
System.out.println("Lesser Than or Equal Result is: "+(a<=d));
}
}
